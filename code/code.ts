function sum(a: number, b: number): number {
    return a + b;
}

function minus(a: number, b: number): number {
    return a - b;
}

function multi(a: number, b: number): number {
    return a * b;
}

function div(a: number, b: number): number {
    return a / b;
}

export {
    sum, minus, multi, div
}